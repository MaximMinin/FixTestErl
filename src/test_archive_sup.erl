-export([init/1]).

%% ====================================================================
%% External functions
%% ====================================================================

start_link(Testcase) ->
    supervisor:start_link(?MODULE, [Testcase]).


%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init([Testcase]) ->
    Child = {test_archive_worker,{test_archive_worker, start_link,[Testcase]},
	      permanent,2000,worker,[test_archive_worker]},
    {ok,{{one_for_all,10,10}, [Child]}}.
